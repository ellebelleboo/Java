public class JavaItsLearning4 {


    public static void main(String args[]) {
        
        
        int number = 7;
        
        if (number % 2 == 0) {
            System.out.println("The number " + number + " is even.");
        } else {
            System.out.println("The number " + number + " is odd.");
        }
    }
}

     
