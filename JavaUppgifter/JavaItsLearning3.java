
public class JavaItsLearning3 {

   
    public static void main(String args[]) {
        int var1 = 12;
        float var2 = 2.0f;
        double var3 = 3;
        char var4 = 'A';
        
        System.out.println("var1 printed as double: " +(double)var1);
        System.out.println("var2 printed as double: " +(double)var2);
        System.out.println("var3 printed as int: " +(int)var3);
        System.out.println("var4 printed as int: " +(int)var4);
        System.out.println("var1 printed as byte: " +(byte)var1);
    }
}
